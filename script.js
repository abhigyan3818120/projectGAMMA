    function buildCard(person, isTeacher) {
      const card = document.createElement('div');
      card.className = 'card' + (isTeacher ? ' card-teacher' : '');


      const artZone = document.createElement('div');
      artZone.className = 'art-zone';
      artZone.dataset.initials = person.name
        .split(' ')
        .slice(0, 2)
        .map(w => w[0])
        .join('')
        .toUpperCase();

      const artGlow = document.createElement('div');
      artGlow.className = 'art-glow';

      const img = document.createElement('img');
      img.className = 'art-img';
      img.alt = person.name;
      img.src = person.photo;
      img.onerror = function () {
        artZone.classList.add('no-photo');
        img.classList.add('hidden-img');
      };

      artZone.appendChild(artGlow);
      artZone.appendChild(img);

      /* Footer */
      const footer = document.createElement('div');
      footer.className = 'card-footer';

      const nameEl = document.createElement('div');
      nameEl.className = 'card-name';
      nameEl.textContent = person.name;

      const roleEl = document.createElement('div');
      roleEl.className = 'card-role';
      roleEl.textContent = person.role;

      footer.appendChild(nameEl);
      footer.appendChild(roleEl);

      card.appendChild(artZone);
      card.appendChild(footer);

      card.addEventListener('click', () => openModal(person));
      return card;
    }
